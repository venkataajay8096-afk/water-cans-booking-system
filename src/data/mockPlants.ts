// ─────────────────────────────────────────────────────────────────────────────
// Helper: Find nearest plants to a given GPS coordinate
// Sorts the full database by Haversine distance and returns the closest N
// ─────────────────────────────────────────────────────────────────────────────
function haversineKm(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLng = ((lng2 - lng1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLng / 2) ** 2;
  return Math.round(R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a)) * 10) / 10;
}

export function getNearbyPlants(
  userLat: number,
  userLng: number,
  maxResults = 8,
  radiusKm = 50          // widen radius so nearby-city plants show up
): WaterPlant[] {
  const withDistances = allWaterPlants
    .map((p) => ({ ...p, distance: haversineKm(userLat, userLng, p.lat, p.lng) }))
    .filter((p) => p.distance <= radiusKm)
    .sort((a, b) => a.distance - b.distance)
    .slice(0, maxResults);

  // Fallback: if nothing within radius, return closest 5 in the whole DB
  if (withDistances.length === 0) {
    return allWaterPlants
      .map((p) => ({ ...p, distance: haversineKm(userLat, userLng, p.lat, p.lng) }))
      .sort((a, b) => a.distance - b.distance)
      .slice(0, 5);
  }

  return withDistances;
}

